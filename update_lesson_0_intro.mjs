async function main() {
  console.log('🔄 Updating lesson 0 with new intro content...');
  
  // Get lesson 0 ID
  const { data: lesson, error: lessonError } = await supabase
    .from('lessons')
    .select('id')
    .eq('index', 0)
    .single();
  
  if (lessonError) {
    console.error('Error finding lesson 0:', lessonError);
    return;
  }
  
  console.log('Found lesson 0 with ID:', lesson.id);
  
  // Delete all existing lines for lesson 0
  const { error: deleteError } = await supabase
    .from('lines')
    .delete()
    .eq('lesson_id', lesson.id);
  
  if (deleteError) {
    console.error('Error deleting existing lines:', deleteError);
    return;
  }
  
  console.log('Deleted existing lines');
  
  // Insert new lines
  const linesToInsert = introLines.map(line => ({
    lesson_id: lesson.id,
    order_num: line.order_num,
    code: `0-${String(line.order_num).padStart(5, '0')}`,
    text_he: line.text_he,
    text_it: line.text_it,
    type: line.type
  }));
  
  const { error: insertError } = await supabase.from('lines').insert(linesToInsert);
  
  if (insertError) {
    console.error('Error inserting lines:', insertError);
    return;
  }
  
  console.log(`✅ Successfully inserted ${linesToInsert.length} INFO lines for lesson 0`);
}

main();
